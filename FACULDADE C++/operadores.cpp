#include <stdio.h>

int main(){

	int	num1 = 10, num2 = 12;
	
	printf("Soma: %d + %d = %d \n", num1, num2, num1+num2);
	printf("Subtracao: %d - %d = %d \n", num1, num2, num1-num2);	
	printf("Multiplicacao: %d x %d = %d \n", num1, num2, num1*num2);
	printf("Divisao: %d / %d = %d \n", num1, num2, num1/num2);
	printf("Resto Divisao: %d %% %d = %d \n", num1, num2, num1%num2);
	
	
	return 0;
}
